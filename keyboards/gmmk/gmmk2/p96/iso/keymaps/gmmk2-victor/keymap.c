void setLayerRGB(int n) {
  switch (n) {
    case 1:
        if(!rgbmode) rgbmode = rgb_matrix_config.mode;
        rgb_matrix_mode_noeeprom(RGB_MATRIX_CUSTOM_layer1);
    break;
    case 2:
        rgb_matrix_mode_noeeprom(RGB_MATRIX_CUSTOM_layer2);
    break;

    case 3:
        rgb_matrix_mode_noeeprom(RGB_MATRIX_CUSTOM_layer3);
    break;

    case 15:
        rgb_matrix_mode_noeeprom(RGB_MATRIX_CUSTOM_layer15);
    break;

  }
};

layer_state_t layer_state_set_user(layer_state_t state) {
  uint8_t layer = biton32(state);
  switch (layer) {
    case _LAYER1:
        setLayerRGB(1);
    break;

    case _LAYER2:
        setLayerRGB(2);
    break;

    case _LAYER3:
        setLayerRGB(3);
    break;

    case _LAYER15:
        setLayerRGB(15);
    break;

    default:
    rgbStatus = rgb_matrix_is_enabled();
    if(rgbStatus == true){
        rgb_matrix_mode_noeeprom(RGB_MATRIX_CUSTOM_layer0);
    }
        
    break;
  }  
  return state;
}



