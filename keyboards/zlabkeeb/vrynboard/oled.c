bool logo_rendered = false;

bool oled_task_kb(void) {
  oled_on();
  if (!oled_task_user()) {
    return false;
  }
  if (!logo_rendered) {
    render_logo();
    if (timer_read() > 5000) logo_rendered = true;
  } else {
    switch (current_display_mode) {
      case 0:
        render_layers();
        break;
      case 1:
        render_animation();
        break;
      case 2:
        render_button_pressed();
        break;
    }
  }

  return false;
}
