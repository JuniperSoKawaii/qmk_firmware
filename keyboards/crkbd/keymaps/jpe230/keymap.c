// saRcASm MOde
bool tog_sarcasm;
bool uppercase;
uint8_t prev_upper;
uint8_t prev_lower;

void keyboard_post_init_kb(void) {
    tog_sarcasm    = false;
    uppercase      = false;
    prev_upper     = 0;
    prev_lower     = 0;
}

bool process_record_sarcasm(uint16_t keycode, keyrecord_t *record) {
    if (keycode == KC_ENTER && record->event.pressed) {
        uppercase = false;
        return true;
    }

    if (uppercase == false && record->event.pressed )  { // Dont start with an uppercase letter on new line
        uppercase = true;
        return true;
    }

    if((KC_A <= keycode) && (keycode <= KC_Z)) {
        if ( record->event.pressed ) {
            bool press = rand() % 2;

            if (prev_upper > 2) {           // if more than 3 lower's in a row print upper
                prev_upper = 0;
                press = false;
            } else if (prev_lower > 2) {    // if more than 3 upper's in a row print lower
                prev_lower = 0;
                press = true;
            }
            if (press) {
                prev_upper++;
                tap_code16(S(keycode));
                clear_mods();
            } else {
                prev_lower++;
                tap_code16(keycode);
            }
        }
        return false;
    }

    return true;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {

#ifdef OLED_ENABLE
    if (record->event.pressed) {
        set_keylog(keycode, record);
    }
#endif

    switch (keycode) {
        case KC_SCASM:
            if (record->event.pressed) {
                tog_sarcasm = !tog_sarcasm;

                if (tog_sarcasm)
                    dprint("Enabling saRCaSm ModE\n");
                else
                    dprint("Disabling saRCaSm ModE\n");

                uppercase = false;
                prev_upper = 0;
                prev_lower = 0;
            }
            return false;
    }

    if (tog_sarcasm) {
        return process_record_sarcasm(keycode, record);
    }

    return true;
}
